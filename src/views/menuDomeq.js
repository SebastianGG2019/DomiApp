function ArmarMarcas(dondeInsertar, array) {
	for (var i = 0; i <= array["length"]-1; i++) {
		var menuUrl = array[i].url;
		var menuContent = array[i].name;
		var menuFirst = array[i].first;
		if (menuFirst == true) {
			dondeInsertar.append("<li><a class='MarcasRec' href="+menuUrl+"><strong>"+menuContent+"</strong></a></li>");
		}else{
			dondeInsertar.append("<li><a href="+menuUrl+">"+menuContent+"</a></li>");
		}
	}
}
